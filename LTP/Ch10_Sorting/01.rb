# compare two words in the unsorted array using <
# the < word should be added to the new, sorted array
# repeat process

# create an unsorted array of strings
scramble = ['cat', 'hat', 'bat', 'sat', 'pat', 'mat']
# create an empty array for sorted words
sorted = []

if 
  scramble[0] < scramble[1]
  first_word = scramble[0]
else 
  first_word = scramble[1]
end
sorted.push first_word

puts sorted